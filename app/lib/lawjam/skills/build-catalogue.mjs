/**
 * Builds catalogue.ts from the SKILL.md folders in this directory.
 * Run after adding/removing a skill:  node app/lib/lawjam/skills/build-catalogue.mjs
 *
 * Produces two exports:
 *   LAWJAM_SKILLS_INDEX — name + description per skill (the part the orchestrator
 *                         peruses to decide which skill fits a task).
 *   LAWJAM_SKILLS_FULL  — the full method body of each skill, applied once chosen.
 *
 * Static-generated (not fs-at-runtime) so it's safe to import anywhere and never
 * bundles Node fs into the client. Re-run this script when the catalogue changes.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dir = path.dirname(fileURLToPath(import.meta.url));

const parseFrontmatter = (md) => {
  const m = md.match(/^---\n([\s\S]*?)\n---/);
  const fm = {};
  if (m) {
    for (const line of m[1].split('\n')) {
      const i = line.indexOf(':');
      if (i > 0) fm[line.slice(0, i).trim()] = line.slice(i + 1).trim();
    }
  }
  return fm;
};

const skills = fs
  .readdirSync(dir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => {
    const skillPath = path.join(dir, d.name, 'SKILL.md');
    if (!fs.existsSync(skillPath)) return null;
    const body = fs.readFileSync(skillPath, 'utf8');
    const fm = parseFrontmatter(body);
    return { id: d.name, name: fm.name || d.name, description: fm.description || '', body };
  })
  .filter(Boolean)
  .sort((a, b) => a.id.localeCompare(b.id));

const index = skills.map((s) => `- **${s.name}**: ${s.description}`).join('\n');
const full = skills.map((s) => `\n### Skill: ${s.name}\n\n${s.body}`).join('\n\n---\n');

const out = `// AUTO-GENERATED by build-catalogue.mjs — do not edit by hand.
// ${skills.length} skill(s): ${skills.map((s) => s.id).join(', ')}
export const LAWJAM_SKILLS_INDEX = ${JSON.stringify(index)};
export const LAWJAM_SKILLS_FULL = ${JSON.stringify(full)};
`;

fs.writeFileSync(path.join(dir, 'catalogue.ts'), out);
console.log(`Wrote catalogue.ts — ${skills.length} skills: ${skills.map((s) => s.id).join(', ')}`);
